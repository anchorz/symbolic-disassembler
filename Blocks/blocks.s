        ld      de,#CONBU
        ld      a,#10
        ld      (de),a
        INPUT_LINE
        jp      c,reset
        inc     de
        ld      a,(de)
        cp      #EMPTY
        jp      z,reset
        ld      hl,#CONBU+2
        ld      a,(hl)
        cp      #'v'
        jr      nz,sp0
        inc     hl
        ld      a,(hl)
        cp      #'p'
        jr      nz,sp0
        ld      (hidden),a
        jr      codeinp
sp0:
        ld      a,(hidden)
        cp      #'p'
        jr      nz,sp1
        ld      hl,#CONBU+2
        ld      a,(hl)
        cp      #'0'
        jr      nz,sp1
        call    atoh
        jr      c,s2
        ld      c,a
        call    atoh
        jr      c,s2
        ld      d,a
        call    atoh
        jr      c,s2
        ld      e,a
        jr      sp2
sp1:
        ld      hl,#CONBU+2
        call    decode
        jr      c,s2
sp2:
        ld      a,c
        cp      #MAX_LEVEL+1
        jr      nc,s2
        ld      b,c
        inc     b
        ld      hl,#lvl0cnt-2
sp2a:
        inc     hl
        inc     hl
        djnz    sp2a
        ld      a,(hl)
        inc     hl
        ld      h,(hl)
        ld      l,a
        or      a
        dec     hl
        sbc     hl,de
        jr      c,s2
        ld      a,c
        ld      (level),a
        ld      (puzzle),de
        jp      reset
s2:
        SET_CURSOR POSITION_INPUT_LINE
        call    prnst0
        TXT_ERROR
        jp      codeinp
reset:
        ld      a,(level)
        ld      c,a
        ld      de,(puzzle)
        call    getpuzzle
        call    unpack
        ld      hl,#-1
        ld      (moves),hl
        ld      hl,#stones
        ld      (custone),hl
        call    show_mv
        ld      a,(level)
        ld      b,a
        inc     b
        ld      de,#Level0_size
        ld      hl,#Level0-Level0_size
res1:
        add     hl,de
        djnz    res1
        ld      bc,#Level0_size
        ld      de,#POSITION_LEVEL
        ldir
        ld      de,#POSITION_PUZZEL
        ld      hl,(puzzle)
        inc     hl
        call    hlkon
        ret
