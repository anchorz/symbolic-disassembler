        ld      a,(level)
        ld      b,a
        inc     b
        ld      hl,#lvl0cnt-2
np1:
        inc     hl
        inc     hl
        djnz    np1
        ld      a,(hl)
        inc     hl
        ld      h,(hl)
        ld      l,a
        dec     hl
        ld      de,(puzzle)
        inc     de
        or      a
        sbc     hl,de
        jr      c,np2
        ld      (puzzle),de
        ret
np2:
        ld      a,(level)
        cp      #MAX_LEVEL
        ret     z
        inc     a
        ld      (level),a
        ld      de,#0
        ld      (puzzle),de
        ret
encode:
        ld      a,r
        and     #MASK_LEVEL_CODE
        add     a,c
        add     a,#'A'
        cp      #'Z'+1
        jr      nc,encode
        ld      h,a
        CALL_OUTA
        ld      a,h
        add     a,a
        add     a,d
        add     a,e
        add     a,#142
        call    outhx
        ld      h,d
        ld      l,e
        add     hl,hl
        add     hl,hl
        ld      a,r
        add     a,a
        and     #MASK_UPPER_BITS
        add     a,h
        ld      h,a
        ld      a,r
        and     #MASK_LOWER_BITS
        add     a,l
        ld      l,a
        call    OUTHL
        ret
