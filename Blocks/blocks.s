        ld      a,0(ix)
        cp      e
        call    c,calc_distance
        exx
        add     ix,bc
        exx
        ld      a,0(ix)
        cp      #FIELD_END
        jr      nz,cu_left1
        ld      (custone),iy
        call    show
        ret
cu_right:
        call    cu_init
        jp      nz,move_right
cu_right1:
        ld      a,e
        cp      0(ix)
        call    c,calc_distance
        exx
        add     ix,bc
        exx
        ld      a,0(ix)
        cp      #FIELD_END
        jr      nz,cu_right1
        ld      (custone),iy
        call    show
        ret
