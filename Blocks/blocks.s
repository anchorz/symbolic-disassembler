sadr:
main:
        jp      start
cmd:
        .ascii  'BLOCKS  '
cmd_end_marker:
        .dw     0x0000
start:
        CURSOR_DISABLE
        ld      hl,#mainpic
        call    decomp
        call    smallwindw
        ld      a,#0
        ld      (level),a
        ld      (hidden),a
        ld      (toggled),a
        ld      hl,#0
        ld      (puzzle),hl
        call    reset
mainloop:
        CURSOR_DISABLE
        UP_INCH
        cp      #'Q'
        jr      z,ende
        ld      hl,#kdo
        ld      bc,#kdotab-kdo
        cpir
        jr      nz,mainloop
        ld      a,#kdotab-kdo-1
        sub     c
        srl     a
        add     a,a
        ld      hl,#kdotab
        ld      c,a
        add     hl,bc
        ld      e,(hl)
        inc     hl
        ld      d,(hl)
        ld      hl,#mainloop
        push    hl
        push    de
        ret
ende:
        call    fullwindw
        SYS_EXIT
vers:
        .ascii  '1/15/2021'
kdo:
        .db     'C',0x00
        .db     'R',0x00
        .db     'E',0x0b
        .db     'S',0x08
        .db     'D',0x09
        .db     'X',0x0a
        .db     'e',0x00
        .db     's',0x00
        .db     'd',0x00
        .db     'x',0x00
        .db     'A','a'
        .db     ' ',0x0d
.if  z1013
        .db     'N',0x00
.endif
kdotab:
        .dw     codeinp
        .dw     reset
        .dw     cu_up
        .dw     cu_left
        .dw     cu_right
        .dw     cu_down
        .dw     move_up
        .dw     move_left
        .dw     move_right
        .dw     move_down
        .dw     next_stone
        .dw     toggle
.if  z1013        
        .dw     solved
.endif
toggle:
        ld      a,(toggled)
        cpl
        ld      (toggled),a
        ret
smallwindw:
        CLS_SMALL_WINDOW
        ret
fullwindw:
        CLS_FULL_WINDOW
        ret
mainpic:
        MAIN_PIC
        DECOMP
codeinp:
        call    prnst0
