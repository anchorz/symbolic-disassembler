        ld      iy,(custone)
        ld      e,0(iy)
        ld      d,1(iy)
        ld      c,#MINIMUM_DISTANCE
        exx
        ld      bc,#STONE_RECT_size
        exx
        ld      ix,#stones
        ld      a,(toggled)
        or      a
        ret
cu_up:
        call    cu_init
        jp      nz,move_up
cu_up1:
        ld      a,1(ix)
        cp      d
        call    c,calc_distance
        exx
        add     ix,bc
        exx
        ld      a,0(ix)
        cp      #FIELD_END
        jr      nz,cu_up1
        ld      (custone),iy
        call    show
        ret
cu_down:
        call    cu_init
        jp      nz,move_down
cu_down1:
        ld      a,d
        cp      1(ix)
        call    c,calc_distance
        exx
        add     ix,bc
        exx
        ld      a,0(ix)
        cp      #FIELD_END
        jr      nz,cu_down1
        ld      (custone),iy
        call    show
        ret
cu_left:
        call    cu_init
        jp      nz,move_left
