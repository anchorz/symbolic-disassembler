        push    bc
        ld      a,0(ix)
        ld      b,e
        sub     b
        jp      p,calc_distance1
        neg
calc_distance1:
        ld      hl,#qtab
        ld      c,a
        ld      b,#0
        add     hl,bc
        ld      a,(hl)
        push    af
        ld      a,1(ix)
        ld      b,d
        sub     b
        jp      p,calc_distance2
        neg
calc_distance2:
        ld      hl,#qtab
        ld      c,a
        ld      b,#0
        add     hl,bc
        ld      a,(hl)
        pop     bc
        add     a,b
        pop     bc
        ret     z
        cp      c
        ret     nc
        ld      c,a
        push    ix
        pop     iy
        ret
