        .db     0xc9
next_stone:
        ld      ix,(custone)
        ld      bc,#STONE_RECT_size
        add     ix,bc
        ld      a,0(ix)
        cp      #FIELD_END
        jr      nz,next_stone1
        ld      ix,#stones
next_stone1:
        ld      (custone),ix
        call    show
        ret
move_up:
        ld      ix,(custone)
        ld      a,2(ix)
        cp      #HORIZONTAL
        ret     nz
        ld      a,1(ix)
        cp      #0
        ret     z
        dec     a
        ld      e,0(ix)
        ld      d,a
        call    calc_pos0
        ld      a,(hl)
        cp      #' '
        ret     nz
        dec     1(ix)
        call    show_mv
        ret
move_down:
        ld      ix,(custone)
        ld      a,2(ix)
        cp      #HORIZONTAL
        ret     nz
        ld      a,1(ix)
        ld      b,3(ix)
        add     a,b
        cp      #FIELD_SIZE/2
        ret     nc
        ld      e,0(ix)
        ld      d,a
        call    calc_pos0
        ld      a,(hl)
        cp      #' '
        ret     nz
        inc     1(ix)
        call    show_mv
        ret
move_left:
        ld      ix,(custone)
        ld      a,2(ix)
        cp      #VERTICAL
        ret     nz
        ld      a,0(ix)
        cp      #0
        ret     z
        dec     a
        ld      e,a
        ld      d,1(ix)
        call    calc_pos0
        ld      a,(hl)
        cp      #' '
        ret     nz
        dec     0(ix)
        call    show_mv
        ret
move_right:
        ld      hl,#stones
        ld      de,(custone)
        or      a
        sbc     hl,de
        jr      nz,mv_right0
        ld      hl,(stones)
        ld      de,#POS_END_42
        or      a
        sbc     hl,de
        jp      z,solved
mv_right0:
        ld      ix,(custone)
        ld      a,2(ix)
        cp      #VERTICAL
        ret     nz
        ld      a,0(ix)
        ld      b,3(ix)
        add     a,b
        cp      #FIELD_SIZE/2
        ret     nc
        ld      e,a
        ld      d,1(ix)
        call    calc_pos0
        ld      a,(hl)
        cp      #' '
        ret     nz
        inc     0(ix)
        call    show_mv
        ret
