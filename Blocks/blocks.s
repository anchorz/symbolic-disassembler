        ld      c,(hl)
        inc     hl
        call    atoh
        ret     c
        ld      (prfsum),a
        call    atoh
        ret     c
        and     #MASK_CAPITAL_LETTERS
        ld      d,a
        call    atoh
        ret     c
        ld      e,a
        srl     d
        rr      e
        srl     d
        rr      e
        ld      a,c
        add     a,a
        add     a,d
        add     a,e
        add     a,#142
        ld      hl,#prfsum
        cp      (hl)
        ret     c
        ld      a,c
        dec     a
        and     #3
        ld      c,a
        ret
